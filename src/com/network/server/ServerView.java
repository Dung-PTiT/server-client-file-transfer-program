    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (isRunning) {
            if (serverProcess != null) {
                if (!serverProcess.isInterrupted()) {
                    serverProcess.interrupt();
                }
                serverProcess = null;
            }
            jButton2.setText("Start Server");
            jLabel5.setText("Server has shutdown");
            isRunning = false;
        } else {
            ServerConfig serverConfig = new ServerConfig();
            serverConfig.port = Integer.parseInt(jTextField1.getText());
            serverConfig.uploadDir = jTextField3.getText();
            serverConfig.username = jTextField2.getText();
            serverConfig.password = String.copyValueOf(jPasswordField1.getPassword());
            serverProcess = new ServerProcess(serverConfig);
            serverProcess.start();
            jButton2.setText("Stop Server");
            jLabel5.setText("Server is running...");
            isRunning = true;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTextField3MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextField3MousePressed
        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Choose upload directory");
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        chooser.setAcceptAllFileFilterUsed(false);
        if (chooser.showOpenDialog(jPanel1) == JFileChooser.APPROVE_OPTION) {
            jTextField3.setText(chooser.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_jTextField3MousePressed

